//Orientação a objetos deve se dar como exemplo os seguintes pilares sendo 4 eles.

//1 - Abstração -
    //Identidade do objeto, se faz necessaria ser unica dentro do projeto para que não haja conflito.
    //Caracteristica do ojeto como as propriedades desse objeto "Pessoa" poderia ser "Peso", "idade".
    //Ação que o objeto ira executar, chamada de metodos, podendo variar de acordo com o tipo de solução.

//2 - Emcapsulamento -
    //Tecnica que adciona segurança à aplicação, tem a capacidade de esconder as propriedades, assim se cria --
    //-- Uma especie de caixa preta.
    //encapsulamento baseado em propriedades privadas, por métodos chamados getters e setters.

//3 - Herança -
    //Possibilidade de herdar as caracteristicas de um objeto
    //**Analogia** Assim, os objetos filhos herdam as características e ações de seus ancestrais”.

//4 - Polimorfismo -
    //Capacidade de alterar a forma de acordo com cada necessidade
    //Permissão de herdar um metode da classe pai, è assim atribuir uma nova implementação.

//Beneficios.

//A programação orientada a objetos propõe uma representação mais fácil de ser compreendida,
// pois a relação de cada elemento em termos de um objeto, ou classe, pode ser comparado ao mundo real.

//Outro benefício da POO é a reutilização de código.

//pois a POO traz representações muito claras de cada um dos elementos que, normalmente, não são interdependentes